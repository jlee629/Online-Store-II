public class Fruit {
    int fruitId, price;
    String fruitName, description, origin;

    public Fruit(int fruitId, String fruitName, int price, String description, String origin) {
        this.fruitId = fruitId;
        this.fruitName = fruitName;
        this.price = price;
        this.description = description;
        this.origin = origin;
    }
}
